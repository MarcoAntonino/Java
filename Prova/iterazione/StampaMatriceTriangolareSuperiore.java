package iterazione;

public class StampaMatriceTriangolareSuperiore {

	public static void main(String[] args) {
		int i,j;
		int n=0;
		
		
		
		
		
		
		
		String msg="";
		
		
		for(i=0;i<n;i=i+1)
		{	
			msg="";
			for (j=0;j<n-1;j++)
			
				msg+=(j<i? " ": "*");
				System.out.println(msg);
			
			
			
		}
		
				
	}

}
